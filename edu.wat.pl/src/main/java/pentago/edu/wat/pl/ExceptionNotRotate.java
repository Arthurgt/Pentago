package pentago.edu.wat.pl;

public class ExceptionNotRotate extends Exception
{
	  public ExceptionNotRotate() { super(); }
	  public ExceptionNotRotate(String message) { super(message); }
	  public ExceptionNotRotate(String message, Throwable cause) { super(message, cause); }
	  public ExceptionNotRotate(Throwable cause) { super(cause); }
}
